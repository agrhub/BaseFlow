// execute_playbook_with_gemini_cli
// ─────────────────────────────────────────────────────────────────────────────
export const executePlaybookWithGeminiTool = new FunctionTool({
  name: 'execute_playbook_with_gemini_cli',
  description:
    'Executes the Gemini CLI headlessly to run a generated playbook markdown file in the workspace, apply code modifications, and automatically create a pull/merge request.',
  parameters: z.object({
    playbookFilePath: z.string().describe('Relative path of the playbook file in the workspace (e.g. "skills/fix_issue_7875.md")'),
    issueNumber: z.string().describe('The ID of the issue being fixed'),
    connectionName: z.string().describe('The name of the active connection profile in BaseFlow'),
    sessionId: z.string().describe('The current chat session ID to stream logs to'),
  }) as any,
  execute: (async ({ playbookFilePath, issueNumber, connectionName, sessionId }: any) => {
    if (!currentScanResult) {
      return 'No repository is scanned. Ask the user to scan a connection first.';
    }

    const repoPath = currentScanResult.repoPath;
    const fullPlaybookPath = path.join(repoPath, playbookFilePath);
    if (!fs.existsSync(fullPlaybookPath)) {
      return `Playbook file not found at: ${playbookFilePath}`;
    }

    const devops = await getDevOpsInfo(connectionName);
    if (!devops) {
      return 'Profile / connection details not found.';
    }

    const token = devops.profile?.options?.gitToken;
    if (!token) {
      return 'Git token is not configured for this connection profile.';
    }

    const hasOauth = !!devops.profile?.agy_auth_code;
    const useVertex = process.env.GOOGLE_GENAI_USE_VERTEXAI === '1' || process.env.GOOGLE_GENAI_USE_VERTEXAI === 'true';
    const hasServiceAccount = !!process.env.GOOGLE_APPLICATION_CREDENTIALS || !!process.env.CLOUD_RUN;
    const hasApiKey = !!(process.env.GOOGLE_API_KEY || process.env.GEMINI_API_KEY);

    if (!hasOauth && !useVertex && !hasApiKey) {
      // Not authenticated! Start the authentication flow automatically!
      const existing = activeAgyAuthProcesses.get(connectionName);
      if (existing) {
        try { existing.child.kill(); } catch (e) {}
        activeAgyAuthProcesses.delete(connectionName);
      }

      const profileDir = getAgyProfileDir(connectionName);
      const env: Record<string, string> = {
        ...process.env,
        USERPROFILE: profileDir,
        HOME: profileDir,
        APPDATA: path.join(profileDir, 'AppData', 'Roaming'),
        LOCALAPPDATA: path.join(profileDir, 'AppData', 'Local'),
        SSH_CLIENT: '127.0.0.1 22 22',
        SSH_TTY: '/dev/pts/0'
      };

      const agyBin = resolveAgyBinary();
      const child = spawn(agyBin, ['models'], {
        env,
        shell: process.platform === 'win32'
      });

      const state = {
        child,
        logBuffer: [] as string[],
        urlSent: false
      };

      activeAgyAuthProcesses.set(connectionName, state);

      return new Promise((resolve) => {
        let resolved = false;

        const handleData = (data: Buffer) => {
          const text = data.toString('utf-8');
          state.logBuffer.push(text);

          const match = text.match(/https:\/\/accounts\.google\.com\/[^\s]*/);
          if (match && !resolved) {
            resolved = true;
            state.urlSent = true;
            resolve(
              `⚠️ **Yêu cầu xác thực Antigravity (Antigravity Auth Required)**\n\n` +
              `Antigravity CLI chưa được cấu hình xác thực cho kết nối "${connectionName}".\n` +
              `Tôi đã tự động bắt đầu luồng đăng nhập Remote SSH OAuth cho bạn.\n\n` +
              `1. Vui lòng nhấp vào liên kết này để đăng nhập:\n` +
              `👉 ${match[0]}\n\n` +
              `2. Copy mã xác thực từ trình duyệt và gửi lại cho tôi bằng cú pháp:\n` +
              `"Mã xác thực Antigravity của tôi là: <mã code>"`
            );
          }
        };

        if (child.stdout) child.stdout.on('data', handleData);
        if (child.stderr) child.stderr.on('data', handleData);

        child.on('close', (code) => {
          if (!resolved) {
            resolved = true;
            activeAgyAuthProcesses.delete(connectionName);
            resolve(`Antigravity CLI requires authentication, but the login flow failed to start. Process exited with code ${code}.`);
          }
        });

        setTimeout(() => {
          if (!resolved) {
            resolved = true;
            resolve(
              `Antigravity CLI requires authentication, but starting the login flow timed out.\n` +
              `Đầu ra hiện tại:\n${state.logBuffer.join('')}`
            );
          }
        }, 5000);
      });
    }

    const sendLog = (text: string) => {
      const stream = activeStreams.get(sessionId);
      if (stream) stream(text);
      process.stdout.write(text);
    };

    try {
      let resultUrl = '';

      await executePlaybookWorkflow({
        conn: connectionName,
        repoPath,
        playbookPath: playbookFilePath,
        issueNumber,
        devops,
        token,
        logLabel: 'Run Playbook Tool',
        onChunk: (chunk) => sendLog(chunk),
        onError: (err) => {
          throw new Error(err);
        },
        onDone: (mrUrl) => {
          resultUrl = mrUrl;
        },
        onFileModified: (filePath) => {
          modifiedFiles.add(filePath);
        },
      });

      if (resultUrl) {
        const providerVerb = devops.provider === 'github' ? 'Pull Request' : 'Merge Request';
        sendLog(`\n\n🎉 **${providerVerb} Created Successfully!**\n[Click here to view it](${resultUrl})\n`);
        return `Playbook executed and ${providerVerb} created at: ${resultUrl}`;
      }

      sendLog(`\n\nNo files were modified by the Gemini CLI.\n`);
      return 'No files were modified by the Gemini CLI.';
    } catch (e: any) {
      sendLog(`\n❌ **Error executing playbook:** ${e.message}\n`);
      return `Failed to execute playbook: ${e.message}`;
    }
  }) as any,
} as any);

